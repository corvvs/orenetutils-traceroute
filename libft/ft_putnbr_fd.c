#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	ssize_t	w;

	(void)w;
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	else
		w = write(fd, "-", n < 0);
	w = write(fd, &("9876543210123456789"[9 + n % 10]), 1);
}
